    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Boleto boleto = new Boleto();
            Fornecedor fornecedor1 = new Fornecedor();
            for (Fornecedor f : new FornecedorDAO().findAll()) {
                if (f.getNome().equals(jComboBox1.getItemAt(jComboBox1.getSelectedIndex()))) {
                    fornecedor1.setNome(f.getNome());
                    fornecedor1.setBanco(f.getBanco());
                    fornecedor1.setId(f.getId());
                }
            }
            boleto.setFornecedor_id(fornecedor1);
            boleto.setCd_barras(cdtxt.getText());
            String value = valortxt.getText().replaceAll(",", ".");
            boleto.setValor(Double.parseDouble(value));
            boleto.setVencimento(datatxt.getText());
            boleto.setId(id);
            if (new BoletoDAO().atualizar(boleto)) {
                JOptionPane.showMessageDialog(null, "Boleto Atualizado com sucesso!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar atualizar boleto!");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro ao tentar atualizar boleto: "+ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

