    private void setarValores() {
        if (!e1.getText().equals("  :  ") && !e2.getText().equals("  :  ") && !e3.getText().equals("  :  ") && !e4.getText().equals("  :  ") && !e5.getText().equals("")){
            valores = new ArrayList<>();
            valores.add(e1.getText());
            valores.add(e2.getText());
            valores.add(e3.getText());
            valores.add(e4.getText());
            valores.add(e5.getText());
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Faltam campos a serem preenchidos.");
        }
    }
    public List<String> getValores(){
        return this.valores;
    }
