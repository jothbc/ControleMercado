    private String verificaStringHorario(String temp) {
        temp = temp.trim();
        //System.out.println("e>>>" + temp);
        if ("".equals(temp) || temp == null || "-".equals(temp)) {
            return "  :  ";
        }
        String[] s = temp.split(":");
        if (Integer.parseInt(s[0]) < 10) {     //s[0] = 12 : s[1] = 21
            s[0] = "0" + s[0];
        }
        //System.out.println("s>>" + s[0] + ":" + s[1]);
        return s[0] + ":" + s[1];
    }

