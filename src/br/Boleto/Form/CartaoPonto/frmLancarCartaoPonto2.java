            txt_hora_DSR.setText(horadsr);
            total += extra + dsr + (double) txt_indenizacao.getValue();
            total -= (double) txt_descontos.getValue();
            double inss = total * (((int) inssSpin.getValue() / (double) 100));
            inss = CDbl.CDblDuasCasas(inss);
