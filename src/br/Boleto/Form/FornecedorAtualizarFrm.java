    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Fornecedor_BuscarJD jd = new Fornecedor_BuscarJD(null, true);
        jd.setVisible(true);
        if (jd.fornecedor != null) {
            forntxt.setText(jd.fornecedor);
            bancotxt.setText(Integer.toString(jd.banco));
            numerotxt.setText(jd.numero);
            id = jd.id;
            forntxt.setEnabled(true);
            bancotxt.setEnabled(true);
            numerotxt.setEnabled(true);
            jButton3.setEnabled(true);
            jButton2.setEnabled(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            Fornecedor f = new Fornecedor();
            f.setNome(forntxt.getText().toUpperCase());
            f.setBanco(Integer.parseInt(bancotxt.getText()));
            f.setNumero(numerotxt.getText());
            f.setId(id);
            FornecedorDAO dao = new FornecedorDAO();
            if (dao.update(f)) {
                JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Falha ao tentar atualizar");
            }
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_jButton2ActionPerformed

