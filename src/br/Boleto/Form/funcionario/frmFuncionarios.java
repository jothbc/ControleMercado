    private void atualizartb() {
        List<Funcionario> funcionarios = new FuncionarioDAO().findAll();
        tb.setRowCount(0);
        for (Funcionario funcionario : funcionarios) {
            try {
                Object[] dado = {funcionario.getNome(), funcionario.getCodigo(), funcionario.getCpf(), funcionario.getPis(), funcionario.getCargo(), funcionario.getAdmissao()};
                if (funcionario.getAtivo() == 1) {
                    tb.addRow(dado);
                }
            } catch (Exception ex) {
                Logger.getLogger(frmFuncionarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

