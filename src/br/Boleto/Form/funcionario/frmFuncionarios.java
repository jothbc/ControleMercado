        if (jTable1.getSelectedRow() < 0) {
            return;
        }
        frmCadastroFuncionario frm = new frmCadastroFuncionario(2);
        Funcionario f = new Funcionario();
        f.setNome((String) tb.getValueAt(jTable1.getSelectedRow(), 0));
        f.setCodigo((int) tb.getValueAt(jTable1.getSelectedRow(), 1));
        try {
            f.setCpf((String) tb.getValueAt(jTable1.getSelectedRow(), 2));
        } catch (Exception ex) {
        }
        try {
            f.setPis((String) tb.getValueAt(jTable1.getSelectedRow(), 3));
        } catch (Exception ex) {
        }
        try {
            f.setCargo((String) tb.getValueAt(jTable1.getSelectedRow(), 4));
        } catch (Exception ex) {
        }
        try {
            f.setAdmissao((Date) tb.getValueAt(jTable1.getSelectedRow(), 5));
        } catch (Exception ex) {
        }
        frm.setartxt(f);
        frm.setVisible(true);
        atualizartb();
