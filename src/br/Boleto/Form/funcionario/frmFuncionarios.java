    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() < 0) {
            return;
        }
        String funcionario = (String) tb.getValueAt(jTable1.getSelectedRow(), 0);
        int op = -1, codigo;
        codigo = (int) tb.getValueAt(jTable1.getSelectedRow(), 1);
        op = JOptionPane.showOptionDialog(this, "Recomendamos não excluir nenhum funcionário da lista!\nDesejas realmente excluir o funcionário " + funcionario + " da lista?", "Exclusão de funcionário.", 1, 1, null, null, null);
        if (op == 0) {
            if (new FuncionarioDAO().remove(codigo)) {
                JOptionPane.showMessageDialog(this, "Removido com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Algo deu errado!");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

