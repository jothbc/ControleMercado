    private void atualizartb() {
        DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
        tb.setRowCount(0);
        List<Funcionario> funcionarios = new FuncionarioDAO().findAll();
        for (Funcionario f: funcionarios){
            if (f.getAtivo()==0){
                Object[] dado = {f.getNome(),f.getCodigo()};
                tb.addRow(dado);
            }
        }
    }
