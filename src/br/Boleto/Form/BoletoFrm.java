    public double totalEmAberto() {
        double soma = 0.0;
        BoletoDAO dao = new BoletoDAO();
        for (Boleto b : dao.findAll()) {
            if (b.getPago() == null) {
                soma += b.getValor();
            }
        }
        return CDbl.CDblDuasCasas(soma);
    }
    
    private void valorEmAbertoTabela() {
        double valorTemp = 0;
        try {
            for (int x = 0; x < jTable1.getRowCount(); x++) {
                if (jTable1.getValueAt(x, 4) == null) {
                    valorTemp += Double.parseDouble(jTable1.getValueAt(x, 3).toString());
                }
            }
        } catch (Exception ex) {
            System.err.println("Erro ao tentar alocar valor na label 'valorAbertotxt': " + ex.getMessage());
            valorTemp = 0;
        }
        String valorString = (Double.toString(CDbl.CDblDuasCasas(valorTemp)));
        valorString = valorString.replaceAll("\\.", ",");
        valorString = colocarPonto(valorString);
        valorAbertotxt.setText(valorString);
        
    }
    
