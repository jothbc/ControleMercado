    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel boletosTable = (DefaultTableModel) jTable1.getModel();
        boletosTable.setRowCount(0);
        BoletoDAO dao = new BoletoDAO();
        CDate conv = new CDate();
        List<Boleto> boletos = dao.findAll();
        String nome, cd_barras;
        Date vencimento_ = null, pago_;
        int id;
        double valor;
        
        Date ini = null, fim = null;
        if (data2txt.getText().equals("  /  /    ")) {
            data2txt.setText(datatxt.getText());
        }
        try {
            ini = conv.DataPTBRStringToDate(datatxt.getText());
        } catch (Exception ex) {
            System.out.println("Boleto--> Botao Filtro Atualizar--> Sem data inicial");
        }
        try {
            fim = conv.DataPTBRStringToDate(data2txt.getText());
        } catch (Exception ex) {
            System.out.println("Boleto--> Botao Filtro Atualizar--> Sem data final");
        }
        
        for (int i = 0; i < boletos.size(); i++) {
            id = boletos.get(i).getId();
            try {
                vencimento_ = conv.DataPTBRStringToDate(DataMySQLtoDataStringPT(boletos.get(i).getVencimento()));
            } catch (Exception ex) {
                Logger.getLogger(BoletoFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
            nome = boletos.get(i).getFornecedor_id().getNome();
            valor = boletos.get(i).getValor();
            valor = CDbl.CDblDuasCasas(valor);
            try {
                pago_ = conv.DataPTBRStringToDate(DataMySQLtoDataStringPT(boletos.get(i).getPago()));
            } catch (Exception ex) {
                pago_ = null;
            }
            cd_barras = boletos.get(i).getCd_barras();
            Object[] dados = {id, vencimento_, nome, valor, pago_, cd_barras, null};
            Date dboleto = null;
            try {
                dboleto = conv.DataMYSQLtoDate(boletos.get(i).getVencimento());
            } catch (Exception ex) {
                Logger.getLogger(BoletoFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
            if ("  /  /    ".equals(datatxt.getText())) {
                if (CheckBoxPagos.isSelected()) {
                    boletosTable.addRow(dados);
                } else if (pago_ == null) {
                    boletosTable.addRow(dados);
                }
            } else if (dboleto.compareTo(ini) >= 0 && dboleto.compareTo(fim) <= 0) {
                if (CheckBoxPagos.isSelected()) {
                    boletosTable.addRow(dados);
                } else if (pago_ == null) {
                    boletosTable.addRow(dados);
                }
            }
//            else if (datatxt.getText().equals(DataMySQLtoDataStringPT(boletos.get(i).getVencimento()))) {
//                boletosTable.addRow(dados);
//            }
        }
        valorEmAbertoTabela();
        totalLinhas();
    }//GEN-LAST:event_jButton1ActionPerformed

